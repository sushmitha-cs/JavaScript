function add(a, b) {
     return a + b;
}
function subtract(a, b) {
        return a - b; 
    }       
let result1 = add(5, 3); // result1 is 8
let result2 = subtract(5, 3); // result2 is 2   
console.log(result1); // prints 8
console.log(result2); // prints 2