//for loop syntax
// for (initialization; condition; increment/decrement) {}
for (let i = 0; i < 5; i++) {
    console.log(i); // prints 0 to 4
}

//syntax for of loop
// for (const value of iterable) {}
let array = [1, 2, 3, 4, 5];    
for (let element of array) {
    console.log(element); // prints 1 to 5
}

