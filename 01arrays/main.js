// print the number of elements in the array

let array = [1, 2, 3, 4, 5];
console.log(array.length);

// push the array element to the end of the array
array.push(6);
console.log(array);
console.log(array.length)

// removes the array element from the end of the array
array.pop()
console.log(array);
console.log(array.length)

// adds the array element at the beginning of the array
array.unshift(0);
console.log(array); 
console.log(array.length); 

// removes the array element from the beginning of the array
array.shift();
console.log(array); 
console.log(array.length); 

